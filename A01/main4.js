// estructura if, else if, else
// si es el -7 que ponga "viva la Pepa"
// si es el 25 que ponga "te la Hinco"

let num = 25;


if (num == 25)
{
    console.log("te la Hinco");
}

else if (num > 0)
{
    console.log("es positivo");
}

else if (num == 0)
{
    console.log("es cero");
}

else if (num == -7)
{
    console.log("viva la Pepa");
}

else
{
    console.log("es negativo");
}
